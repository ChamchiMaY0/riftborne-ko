function Escape-CsvValue($value) {
    $text = [string]$value
    if ($text -match '[,"\r\n]') {
        return '"' + $text.Replace('"', '""') + '"'
    }
    return $text
}

function Export-CsvRows($rows, $headers, $path) {
    $lines = New-Object System.Collections.Generic.List[string]
    $lines.Add((@($headers | ForEach-Object { Escape-CsvValue $_ }) -join ","))
    foreach ($row in $rows) {
        $values = foreach ($header in $headers) { $row[$header] }
        $lines.Add((@($values | ForEach-Object { Escape-CsvValue $_ }) -join ","))
    }
    [System.IO.File]::WriteAllLines($path, $lines, $Utf8NoBom)
}

function Write-Utf8Text($path, $text) {
    $normalized = ($text.Trim() -replace "`r`n", "`n") + "`n"
    [System.IO.File]::WriteAllText($path, $normalized, $Utf8NoBom)
}

