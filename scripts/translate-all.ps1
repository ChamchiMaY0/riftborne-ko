function Translate-Consequence($text) {
    if ([string]::IsNullOrWhiteSpace($text)) { return "" }
    $parts = @()
    foreach ($part in ($text -split ";")) {
        $p = $part.Trim()
        if ($p -match "^(.*?) relation ([+-]\d+)$") {
            $name = $matches[1]
            if ($factionMap.ContainsKey($name)) { $name = $factionMap[$name] }
            $parts += "$name 관계 $($matches[2])"
        }
        elseif ($p -match "^gain ([\d,]+) ([A-Za-z]+)$") {
            $res = $matches[2]
            if ($resourceMap.ContainsKey($res)) { $res = $resourceMap[$res] }
            $parts += "$res $($matches[1]) 획득"
        }
        elseif ($p -match "^lose ([\d,]+) ([A-Za-z]+)$") {
            $res = $matches[2]
            if ($resourceMap.ContainsKey($res)) { $res = $resourceMap[$res] }
            $parts += "$res $($matches[1]) 손실"
        }
        else {
            $parts += $p
        }
    }
    return ($parts -join "; ")
}

