조직은 장기 영향력을 숫자로 바꾸는 구조입니다. 계약은 다른 플레이어의 행동을 자원, 시간, 보상으로 묶습니다.

좋은 계약:
- 목표가 분명합니다.
- 보상이 행동 비용에 맞습니다.
- 완료 조건이 오해를 줄입니다.

지분
----
지분은 장기 신뢰와 기대의 숫자입니다. 공포 매도는 보통 다른 사람에게 보상합니다.

외교
----
